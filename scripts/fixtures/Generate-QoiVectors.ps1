$lines.Add('  [')
foreach ($schedule in $data.stream_schedules.output) {
  $capacities = (@($schedule.capacities) | ForEach-Object { "${_}UL" }) -join ', '
  $lines.Add(('    ("{0}", [{1}]),' -f $schedule.id, $capacities))
