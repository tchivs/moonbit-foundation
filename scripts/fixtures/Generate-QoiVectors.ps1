$lines.Add('')
$lines.Add('///|')
$lines.Add('fn _generated_qoi_adversarial_ids() -> Array[String] {')
$ids = (@($data.adversarial_cases) | ForEach-Object { '"' + $_.id + '"' }) -join ', '
