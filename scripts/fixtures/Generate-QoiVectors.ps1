$lines.Add('fn _generated_qoi_input_schedules() -> Array[(String, Array[UInt64])] {')
$lines.Add('  [')
foreach ($schedule in $data.stream_schedules.input) {
  $capacities = (@($schedule.capacities) | ForEach-Object { "${_}UL" }) -join ', '
